func TestExtractSessionID_FromBody(t *testing.T) {
	body := []byte(`{"model":"claude-3-5-sonnet","session_id":"sess-abc123","messages":[]}`)
	req, _ := http.NewRequest(http.MethodPost, "/v1/messages", nil)
	sid := extractSessionID(req, body)
	assert.Equal(t, "sess-abc123", sid)
}

func TestExtractSessionID_FromHeader(t *testing.T) {
	req, _ := http.NewRequest(http.MethodPost, "/v1/messages", nil)
	req.Header.Set("X-Session-Id", "header-session-xyz")
	sid := extractSessionID(req, nil)
	assert.Equal(t, "header-session-xyz", sid)
}

func TestExtractSessionID_AutoGenerated(t *testing.T) {
	req, _ := http.NewRequest(http.MethodPost, "/v1/messages", nil)
	sid := extractSessionID(req, nil)
	assert.True(t, strings.HasPrefix(sid, "auto-session-"), "expected auto-session- prefix, got: %s", sid)
}

func TestExtractSessionID_BodyTakesPriorityOverHeader(t *testing.T) {
	body := []byte(`{"session_id":"body-session"}`)
	req, _ := http.NewRequest(http.MethodPost, "/v1/messages", nil)
	req.Header.Set("X-Session-Id", "header-session")
	sid := extractSessionID(req, body)
	assert.Equal(t, "body-session", sid)
}


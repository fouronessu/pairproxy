func TestResolveModelToTarget_ExactMatch(t *testing.T) {
	targets := []lbTarget{
		{id: "t1", supportedModels: []string{"claude-3-5-sonnet-20241022"}},
		{id: "t2", supportedModels: []string{"gpt-4o"}},
	}
	result := resolveModelToTarget("gpt-4o", targets, []string{"t1", "t2"})
	assert.Equal(t, "t2", result)
}

func TestResolveModelToTarget_WildcardMatch(t *testing.T) {
	targets := []lbTarget{
		{id: "t1", supportedModels: []string{"claude-3-5-*"}},
		{id: "t2", supportedModels: []string{"gpt-4o"}},
	}
	result := resolveModelToTarget("claude-3-5-sonnet-20241022", targets, []string{"t1", "t2"})
	assert.Equal(t, "t1", result)
}

func TestResolveModelToTarget_NoMatch_FallbackToFirst(t *testing.T) {
	targets := []lbTarget{
		{id: "t1", supportedModels: []string{"model-A"}},
		{id: "t2", supportedModels: []string{"model-B"}},
	}
	result := resolveModelToTarget("model-unknown", targets, []string{"t1", "t2"})
	assert.Equal(t, "t1", result, "should fall back to first target when no match")
}

func TestResolveModelToTarget_NoSupportedModels_MatchesAll(t *testing.T) {
	targets := []lbTarget{
		{id: "t1", supportedModels: []string{"model-A"}},
		{id: "t2", supportedModels: nil}, // no restriction → matches all
	}
	result := resolveModelToTarget("anything", targets, []string{"t1", "t2"})
	// t1 doesn't match, t2 has no restriction → t2
	assert.Equal(t, "t2", result)
}


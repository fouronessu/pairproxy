func TestModelRouterClient_Route_PreservesOriginalModel(t *testing.T) {
	var capturedBody map[string]interface{}
	srv, client := newTestRouterServer(t, func(w http.ResponseWriter, r *http.Request) {
		json.NewDecoder(r.Body).Decode(&capturedBody) //nolint:errcheck
		w.Header().Set("Content-Type", "application/json")
		json.NewEncoder(w).Encode(map[string]interface{}{ //nolint:errcheck
			"model_rankings": []map[string]interface{}{{"rank": 1, "model": "gpt-4o", "score": 1.0}},
		})
	})
	_ = srv

	body := []byte(`{"model":"claude-3-5-sonnet-20241022","messages":[]}`)
	client.Route(context.Background(), "r1", "u1", "s1", body, "claude-3-5-sonnet-20241022", nil) //nolint:errcheck

	// Model should NOT be changed to "auto" — original model preserved
	assert.Equal(t, "claude-3-5-sonnet-20241022", capturedBody["model"],
		"original model should be preserved, not changed to 'auto'")
}

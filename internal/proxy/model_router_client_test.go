func TestModelRouterClient_Route_Timeout(t *testing.T) {
	logger := zaptest.NewLogger(t)
	// Create a slow server
	slow := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		time.Sleep(200 * time.Millisecond)
		w.WriteHeader(http.StatusOK)
	}))
	defer slow.Close()

	// Client with 50ms timeout
	client := NewModelRouterClient(config.ModelRouterConfig{
		URL:     slow.URL,
		Timeout: 50 * time.Millisecond,
	}, logger)

	_, err := client.Route(context.Background(), "req-t", "user", "sess", nil, "", nil)
	assert.Error(t, err, "should time out")
}


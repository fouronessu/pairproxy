func TestExpandCandidateModels_FiltersWildcards(t *testing.T) {
	targets := []lbTarget{
		{id: "t1", supportedModels: []string{"claude-3-5-sonnet-20241022", "claude-3-*"}},
		{id: "t2", supportedModels: []string{"gpt-4o", "*"}},
		{id: "t3", supportedModels: []string{"DeepSeek-V3"}},
	}
	ids := []string{"t1", "t2", "t3"}
	models := expandCandidateModels(targets, ids)

	// 通配符 "claude-3-*" 和 "*" 应被过滤
	assert.Contains(t, models, "claude-3-5-sonnet-20241022")
	assert.Contains(t, models, "gpt-4o")
	assert.Contains(t, models, "DeepSeek-V3")
	for _, m := range models {
		assert.False(t, strings.Contains(m, "*"), "wildcard should be filtered: %s", m)
	}
}

func TestExpandCandidateModels_Deduplicated(t *testing.T) {
	targets := []lbTarget{
		{id: "t1", supportedModels: []string{"gpt-4o", "gpt-4-turbo"}},
		{id: "t2", supportedModels: []string{"gpt-4o", "gpt-3.5-turbo"}},
	}
	models := expandCandidateModels(targets, []string{"t1", "t2"})

	count := 0
	for _, m := range models {
		if m == "gpt-4o" {
			count++
		}
	}
	assert.Equal(t, 1, count, "gpt-4o should appear only once after deduplication")
}

func TestExpandCandidateModels_OnlyIncludesRequestedIDs(t *testing.T) {
	targets := []lbTarget{
		{id: "t1", supportedModels: []string{"model-A"}},
		{id: "t2", supportedModels: []string{"model-B"}},
		{id: "t3", supportedModels: []string{"model-C"}},
	}
	models := expandCandidateModels(targets, []string{"t1", "t3"}) // t2 excluded
	assert.Contains(t, models, "model-A")
	assert.Contains(t, models, "model-C")
	assert.NotContains(t, models, "model-B")
}

func TestExpandCandidateModels_EmptySupportedModels_Skipped(t *testing.T) {
	targets := []lbTarget{
		{id: "t1", supportedModels: nil},        // no restriction
		{id: "t2", supportedModels: []string{"gpt-4o"}},
	}
	models := expandCandidateModels(targets, []string{"t1", "t2"})
	// t1 has no supported models, it's skipped (can't contribute to candidate list)
	assert.Equal(t, []string{"gpt-4o"}, models)
}

